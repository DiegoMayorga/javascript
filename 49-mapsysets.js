/* MAPS Y SETS */

// ARRAY
// Almacena datos (anidados) de cualquier tipo y tamaño.
// Es iterable, también muchos métodos de arreglos disponibles.
// El orden es garantizado, duplicar está permitido, índice basado en cero para acceder a elementos.

// SETS
// Almacena datos anidados de cualquier tipo y tamaño.
// Iterable y también con algunos métodos disponibles de conjuntos.
// El orden no es garantizado, los duplicados no están disponibles y no tienen acceso basado en índices.

// MAPS
// Almacena datos clave-valor de cualquier tipo y tamaño y permite cualquier key value.
// Iterable, también con algunos métodos disponibles de maps.
// El orden es garantizado, las llaves duplicadas no están disponibles y tienen acceso basado en llaves.