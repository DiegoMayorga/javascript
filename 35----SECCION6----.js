// Recapitulando lo que sé sobre funciones
// 1. Las funciones son "código bajo demanda"
// 2. Las variables y constantes creadas en funciones solo aplican dentro de la función
// 3. Las funciones pueden tomar parámetros (argumentos) y pueden retornar un valor
// 4. Las funciones pueden ser llamadas varias veces (con diferentes argumentos)
// 5. Las funciones pueden ser llamadas directamente e indirectamente (indirectas por ejemplo al presionar un botón)

// Parámetros vs Argumentos
// Técnicamente hay una diferencia entre ellos:
// Los parámetros son esas variables que especifico entre paréntesis al definir una función.
// function sayHi(name) {...}
// En este ejemplo, name es un parámetro.
// Los argumentos son los valores concretos que paso a una función cuando la invoco.
// sayHi("Diego");
// "Diego" es un argumento de la función. Para el parámetro name para ser más precisos.

// Dado que ambos conceptos obviamente están muy conectados, es normal decir: "Vamos a definir qué argumentos recibe una función"
// o algo por el estilo, desde definir los argumentos de una función al final significa que yo configuro los parámetros (y viceversa).
