/* Estilizando elementos del DOM */

// 1. A través de la propiedad style
// Directamente apunta a estilos css individuales en el elemento.
// Controla estilos como estilos inline en el elemento.
// Los nombres de las propiedades style son basados en las propiedades css, pero con algunos ajustes (ejemplo: backgroundColor).

// 2. A través del className
// Directamente le da las clases CSS asignadas al elemento.
// Controla todas las clases a la vez.
// También puedo controlar el id u otras propiedades.

// 3. Vía classList
// Convenientemente agrega, elimina o cambia clases CSS.
// Control detallado sobre las clases que se agregan.
// Puede ser usado con className (con cuidado).