/* typeof */

// typeof me permite identificar el tipo de un valor.

typeof "Hola" // "string"
typeof 10 // "number"
typeof true // "boolean"
typeof undefined // "undefined"
typeof null // "object"
typeof [] // "object"
typeof {} // "object"
typeof function(){} // "function"
typeof NaN // "number"

// typeof devuelve "object" para arrays y objetos.
// typeof devuelve "function" para funciones.
