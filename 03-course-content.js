/* ¿Qué hay en este curso? */

// En este curso hay ciertas divisiones. Los conceptos básicos, construir una base fuerte y conceptos avanzados.

// En los conceptos básicos se verá lo básico del lenguaje, sintaxis base, desarrollo eficiente y debugging, estructuras de control (condiciones, bucles, ...)
// Detrás de escenas de JS y una mirada cercana a funciones, lo básico del DOM, arreglos y objetos iterables.

// En la construcción de una base sólida, veré clases, programación orientada a objetos, funciones constructoras y prototipos, más sobre el DOM y APIs del navegador,
// eventos, navegación profunda sobre funciones, más sobre algunos números y strings, código asíncrono y sobre HTTP (con ajax).

// Y en cuanto a los conceptos avanzados, veré librerías de terceros, módulos JavaScript, herramientas como webpack y babel... Trabajaré con almacenamiento en el navegador,
// soporte del navegador, frameworks javascript, meta programación, introducción a NodeJS, seguridad, despliegue, optimización de rendimiento y pérdidas de memoria.

